class Main 
{
//Class Declaration: class-delclaration name-of-class
  public static void main (String[] args) 
  {
    System.out.println("Hello world!");
    System.out.println("\\Sean Lee\\");
    //"\\" outputs the character after the first backslash, in this case that character is "\"
    System.out.println("\n");
    //"\n" creates a new line
    System.out.println("\t");
    //"\t" acts as the tab key
    System.out.println("  /\\");
    System.out.println(" /  \\");
    System.out.println("/    \\");
    System.out.print("_____");
  }
}
